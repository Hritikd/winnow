"""Reproducible benchmark: does winnow keep the *relevant* chunks under budget?

We compare four context-selection strategies at fixed token budgets:

* ``winnow``    — BM25 relevance + MMR diversity (this library, lambda=0.7)
* ``bm25``      — pure BM25 relevance packing (winnow with lambda=1.0)
* ``head``      — keep chunks from the start until the budget is hit
                  (the naive "just truncate the context" default)
* ``random``    — seeded random packing (a sanity-check floor)

Metric: **gold recall** — the fraction of human-labeled relevant ("gold")
chunks that survive compression. Higher is better. We also report the average
token budget used. Everything is deterministic and runs offline.

Usage:
    python benchmarks/bench.py              # print a markdown table
    python benchmarks/bench.py --write       # also update benchmarks/results.md
"""

from __future__ import annotations

import argparse
import json
import random
from collections.abc import Sequence
from pathlib import Path

from winnow import Chunk, compress, get_tokenizer

DATA = Path(__file__).parent / "data.json"
RESULTS = Path(__file__).parent / "results.md"
BUDGET_RATIOS = (0.25, 0.40, 0.60)
TOKENIZER = get_tokenizer(prefer_tiktoken=False)  # heuristic, so CI is hermetic


def _pack_in_order(
    order: Sequence[int], ids: list[str], counts: list[int], budget: int
) -> set[str]:
    """Greedily keep chunks in ``order`` until the budget is hit; return ids."""
    kept: set[str] = set()
    used = 0
    for i in order:
        if used + counts[i] <= budget:
            kept.add(ids[i])
            used += counts[i]
    return kept


def _recall(kept_ids: set[str], gold: list[str]) -> float:
    if not gold:
        return 1.0
    return sum(1 for g in gold if g in kept_ids) / len(gold)


def run() -> dict[str, dict[str, float]]:
    cases = json.loads(DATA.read_text())
    methods = ("winnow", "bm25", "head", "random")
    # accumulators: method -> ratio -> [recalls]
    recall_acc = {m: {r: [] for r in BUDGET_RATIOS} for m in methods}

    for case in cases:
        chunks = [Chunk(id=c["id"], text=c["text"]) for c in case["chunks"]]
        ids = [c.id for c in chunks]
        counts = [TOKENIZER.count(c.text) for c in chunks]
        total = sum(counts)
        gold = case["gold"]

        for ratio in BUDGET_RATIOS:
            budget = int(total * ratio)

            w = compress(case["query"], chunks, budget, lambda_=0.7, tokenizer=TOKENIZER)
            recall_acc["winnow"][ratio].append(_recall(set(w.kept_ids), gold))

            b = compress(case["query"], chunks, budget, lambda_=1.0, tokenizer=TOKENIZER)
            recall_acc["bm25"][ratio].append(_recall(set(b.kept_ids), gold))

            head_ids = _pack_in_order(list(range(len(chunks))), ids, counts, budget)
            recall_acc["head"][ratio].append(_recall(head_ids, gold))

            rng = random.Random(1234 + len(chunks))
            order = list(range(len(chunks)))
            rng.shuffle(order)
            rand_ids = _pack_in_order(order, ids, counts, budget)
            recall_acc["random"][ratio].append(_recall(rand_ids, gold))

    summary: dict[str, dict[str, float]] = {}
    for m in methods:
        summary[m] = {}
        for ratio in BUDGET_RATIOS:
            rs = recall_acc[m][ratio]
            summary[m][f"recall@{int(ratio * 100)}"] = sum(rs) / len(rs)
    return summary


def to_markdown(summary: dict[str, dict[str, float]]) -> str:
    cols = sorted(next(iter(summary.values())).keys())
    titles = [c.replace("recall@", "gold recall @ ") + "% budget" for c in cols]
    header = "| method | " + " | ".join(titles) + " |"
    sep = "|" + "---|" * (len(cols) + 1)
    rows = []
    label = {"winnow": "**winnow** (relevance + diversity)", "bm25": "bm25 (relevance only)",
             "head": "head-truncation (naive default)", "random": "random"}
    for m in ("winnow", "bm25", "head", "random"):
        cells = " | ".join(f"{summary[m][c]:.2f}" for c in cols)
        rows.append(f"| {label[m]} | {cells} |")
    return "\n".join([header, sep, *rows])


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--write", action="store_true", help="update benchmarks/results.md")
    args = parser.parse_args()

    summary = run()
    table = to_markdown(summary)
    print(table)

    if args.write:
        body = (
            "# Benchmark results\n\n"
            "Generated by `python benchmarks/bench.py --write`. Deterministic; "
            "uses the dependency-free heuristic tokenizer so anyone reproduces "
            "the same numbers.\n\n"
            "**Gold recall** = fraction of human-labeled relevant chunks that "
            "survive compression to the given token budget (higher is better). "
            "Dataset: `benchmarks/data.json` (8 retrieval cases where the "
            "relevant passages are *scattered*, not at the front).\n\n"
            f"{table}\n"
        )
        RESULTS.write_text(body)
        print(f"\nwrote {RESULTS}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
